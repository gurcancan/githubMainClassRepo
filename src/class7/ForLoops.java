package class7;

public class ForLoops {
    public static void main(String[] args) {

        /*
        prints number from 0 to 9
         */

        int number=0;
        while (number<10){
            System.out.println(number);
            number++;
        }

        System.out.println("***********");
        for (int num=0;num<10;num++){
            System.out.println(num);
        }

    }
}
