package class6;

public class WhileLoop {
    public static void main(String[] args) {

        int number=0;
        if (number<3){
            System.out.println("Hello World");
        }
        number++;
        if (number<3){
            System.out.println("Hello World");
        }
        number++;
        if (number<3){
            System.out.println("Hello World");
        }

        int number2=0;
        while (number2<10){
            System.out.println("Hello World");
            number2++;
        }

    }
}
